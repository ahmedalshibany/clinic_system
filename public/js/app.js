class ToastSystem {
    constructor() {
        this.container = null;
        this.init();
    }

    init() {
        if (!document.querySelector('.toast-container')) {
            this.container = document.createElement('div');
            this.container.className = 'toast-container';
            document.body.appendChild(this.container);
        } else {
            this.container = document.querySelector('.toast-container');
        }
    }

    show(message, type = 'info', title = null) {
        if (!this.container) this.init();

        const icons = {
            success: 'fa-check-circle',
            error: 'fa-exclamation-triangle',
            warning: 'fa-exclamation-triangle',
            info: 'fa-info-circle'
        };

        const titles = {
            success: 'Success',
            error: 'Error',
            warning: 'Warning',
            info: 'Information'
        };

        let displayTitle = title || titles[type] || 'Notification';
        if (typeof translations !== 'undefined' && app && app.lang) {
            const toastKey = 'toast' + type.charAt(0).toUpperCase() + type.slice(1);
            if (translations[app.lang] && translations[app.lang][toastKey]) {
                displayTitle = translations[app.lang][toastKey];
            }
        }

        const icon = icons[type] || icons.info;

        const toast = document.createElement('div');
        toast.className = `toast-notification ${type}`;

        toast.innerHTML = `
            <div class="toast-icon">
                <svg class="toast-timer-ring" viewBox="0 0 24 24">
                    <circle cx="12" cy="12" r="10" />
                </svg>
                <i class="fas ${icon}"></i>
            </div>
            <div class="toast-content">
                <div class="toast-title">${displayTitle}</div>
                <div class="toast-message">${message}</div>
            </div>
            <div class="toast-close" onclick="this.parentElement.remove()">
                <i class="fas fa-times"></i>
            </div>
        `;

        this.container.appendChild(toast);

        setTimeout(() => {
            if (toast.parentElement) {
                toast.classList.add('closing');
                toast.addEventListener('animationend', () => {
                    if (toast.parentElement) toast.remove();
                });
            }
        }, 3000);
    }

    success(message) { this.show(message, 'success'); }
    error(message) { this.show(message, 'error'); }
    warning(message) { this.show(message, 'warning'); }
    info(message) { this.show(message, 'info'); }
}

window.toast = new ToastSystem();
