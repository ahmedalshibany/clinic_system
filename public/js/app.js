class App {
    constructor() {
        this.lang = localStorage.getItem('clinic_lang') || document.documentElement.lang || 'en';
        this.theme = localStorage.getItem('clinic_theme') || 'light';
    }

    init() {
        this.applyLanguage(this.lang);
        this.applyTheme(this.theme);
        this.bindEvents();
        this.setupGlobalErrorHandlers();

        $(document).on('layout-loaded', () => {
            this.applyLanguage(this.lang);
            this.applyTheme(this.theme);
        });
    }

    setupGlobalErrorHandlers() {
        window.addEventListener('error', (e) => {
            console.error('Global error tracked:', e.error);
        });

        window.addEventListener('unhandledrejection', (e) => {
            console.error('Unhandled promise rejection:', e.reason);
            e.preventDefault();
        });
    }

    initCharts() {
    }

    bindEvents() {
        $(document).on('click', '#langToggle', (e) => {
            this.toggleLanguage();
        });

        $(document).on('click', '#themeToggle', (e) => {
            this.toggleTheme();
        });

        $(document).on('click', '#sidebarToggle', (e) => {
            $('.sidebar').toggleClass('active');
        });


    }

    toggleLanguage() {
        const newLang = this.lang === 'en' ? 'ar' : 'en';
        localStorage.setItem('clinic_lang', newLang);
        document.cookie = 'clinic_lang=' + newLang + '; path=/; max-age=' + (60*60*24*365);
        const url = new URL(window.location.href);
        url.searchParams.set('lang', newLang);
        window.location.href = url.toString();
    }

    toggleTheme() {
        this.theme = this.theme === 'light' ? 'dark' : 'light';
        localStorage.setItem('clinic_theme', this.theme);
        this.applyTheme(this.theme);
    }

    applyTheme(theme) {
        $('html').attr('data-theme', theme);
        const $btn = $('#themeToggle');
        if ($btn.length) {
            $btn.html(theme === 'dark' ? '<i class="fas fa-sun text-warning"></i>' : '<i class="fas fa-moon"></i>');
        }
    }

    applyLanguage(lang) {
        $('html').attr('lang', lang);
        $('html').attr('dir', lang === 'ar' ? 'rtl' : 'ltr');

        $('[data-i18n]').each(function () {
            const $el = $(this);
            const key = $el.data('i18n');
            const attrKey = $el.attr('data-i18n');

            if (translations[lang] && translations[lang][attrKey]) {
                const text = translations[lang][attrKey];

                if ($el.is('input') || $el.is('textarea')) {
                    $el.attr('placeholder', text);
                } else {
                    if ($el.children().length > 0) {
                        const contents = $el.contents();
                        const lastNode = contents.last()[0];
                        if (lastNode && lastNode.nodeType === 3) {
                            lastNode.textContent = text;
                        } else {
                            const el = this;
                            if (el.children.length > 0) {
                                let hasText = false;
                                for (let i = 0; i < el.childNodes.length; i++) {
                                    if (el.childNodes[i].nodeType === 3 && el.childNodes[i].nodeValue.trim().length > 0) {
                                        el.childNodes[i].nodeValue = " " + text;
                                        hasText = true;
                                        break;
                                    }
                                }
                                if (!hasText) {
                                    if (el.lastChild && el.lastChild.nodeType === 3) {
                                        el.lastChild.textContent = text;
                                    }
                                }
                            } else {
                                $el.text(text);
                            }
                        }
                    } else {
                        $el.text(text);
                    }
                }
            }
        });

        $('[data-i18n-placeholder]').each(function () {
            const $el = $(this);
            const key = $el.attr('data-i18n-placeholder');
            if (translations[lang][key]) {
                $el.attr('placeholder', translations[lang][key]);
            }
        });

        $('[data-i18n-title]').each(function () {
            const $el = $(this);
            const key = $el.attr('data-i18n-title');
            if (translations[lang][key]) {
                $el.attr('title', translations[lang][key]);
            }
        });

        $('#langToggleText').text(translations[lang].langBtn);
        this.updateDynamicGreeting();
    }

    updateDynamicGreeting() {
        const $greeting = $('#dashboard-greeting');
        if (!$greeting.length) return;

        const hour = new Date().getHours();
        const lang = this.lang;

        let key, icon;
        if (hour >= 5 && hour < 12) {
            key = 'goodMorning';
            icon = 'fa-sun';
        } else if (hour >= 12 && hour < 17) {
            key = 'goodAfternoon';
            icon = 'fa-cloud';
        } else if ((hour >= 17 && hour <= 23) || (hour >= 0 && hour <= 4)) {
            key = 'goodEvening';
            icon = 'fa-moon';
        } else {
            key = 'goodEvening';
            icon = 'fa-moon';
        }

        const text = translations[lang]?.[key] || translations['en']?.[key] || key;
        $greeting.text(text);
        $('#greeting-icon').attr('class', 'fas ' + icon);
    }

    showAlert(message, type) {
        if (typeof toast !== 'undefined') {
            toast.show(message, type);
        } else {
            const $alert = $('<div>')
                .addClass(`alert alert-${type} custom-alert show`)
                .text(message)
                .appendTo('body')
                .show();

            setTimeout(() => {
                $alert.fadeOut(300, function () { $(this).remove(); });
            }, 3000);
        }
    }
}

